        // https://github.com/KdotJPG/OpenSimplex2/blob/master/csharp/OpenSimplex2S.cs#L493
        public static float Noise(long seed, double x, double y, double z, double w)
        {
            var xyz = x + y + z;
            var ww = w * 1.118033988749894;
            var s2 = xyz * -0.16666666666666666 + ww;
            var xs = x + s2;
            var ys = y + s2;
            var zs = z + s2;
            var ws = -0.5 * xyz + ww;

            return NoiseUnskewedBase(seed, xs, ys, zs, ws);
        }

        // https://github.com/KdotJPG/OpenSimplex2/blob/master/csharp/OpenSimplex2S.cs#L521
        static float NoiseUnskewedBase(long seed, double xs, double ys, double zs, double ws)
        {
            // Get base points and offsets
            var xsb = (int)Math.Floor(xs);
            var ysb = (int)Math.Floor(ys);
            var zsb = (int)Math.Floor(zs);
            var wsb = (int)Math.Floor(ws);
            var xsi = (float)(xs - xsb);
            var ysi = (float)(ys - ysb);
            var zsi = (float)(zs - zsb);
            var wsi = (float)(ws - wsb);

            // Unskewed offsets
            var ssi = (xsi + ysi + zsi + wsi) * UnSkew4D;
            var xi = xsi + ssi;
            var yi = ysi + ssi;
            var zi = zsi + ssi;
            var wi = wsi + ssi;

            // Prime pre-multiplication for hash.
            var xsvp = xsb * PrimeX;
            var ysvp = ysb * PrimeY;
            var zsvp = zsb * PrimeZ;
            var wsvp = wsb * PrimeW;

            // Index into initial table.
            var index = (((int)Math.Floor(xs * 4) & 3) << 0)
                | (((int)Math.Floor(ys * 4) & 3) << 2)
                | (((int)Math.Floor(zs * 4) & 3) << 4)
                | (((int)Math.Floor(ws * 4) & 3) << 6);

            // Point contributions
            var value = 0.0F;
            var (secondaryIndexStart, secondaryIndexStop) = Lookup4DA[index];
            for (int i = secondaryIndexStart; i < secondaryIndexStop; i++)
            {
                var c = Lookup4DB[i];
                var dx = xi + c.dx;
                var dy = yi + c.dy;
                var dz = zi + c.dz;
                var dw = wi + c.dw;
                float a = (dx * dx + dy * dy) + (dz * dz + dw * dw);
                if (a < Rsquared4D)
                {
                    a -= Rsquared4D;
                    a *= a;
                    value += a * a * Grad(seed, xsvp + c.xsvp, ysvp + c.ysvp, zsvp + c.zsvp, wsvp + c.wsvp, dx, dy, dz, dw);
                }
            }
            return value;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        static float Grad(long seed, long xsvp, long ysvp, long zsvp, long wsvp, float dx, float dy, float dz, float dw)
        {
            var hash = seed ^ (xsvp ^ ysvp) ^ (zsvp ^ wsvp);
            hash *= HashMultiplier;
            hash ^= hash >> (64 - NGrads4DExponent + 2);
            var gi = (int)hash & ((NGrads4D - 1) << 2);
            return (Gradients4D[gi | 0] * dx + Gradients4D[gi | 1] * dy) + (Gradients4D[gi | 2] * dz + Gradients4D[gi | 3] * dw);
        }

        class LatticeVertex4D
        {
            public readonly float dx;
            public readonly float dy;
            public readonly float dz;
            public readonly float dw;

            public readonly long xsvp;
            public readonly long ysvp;
            public readonly long zsvp;
            public readonly long wsvp;

            public LatticeVertex4D(int xsv, int ysv, int zsv, int wsv)
            {
                xsvp = xsv * PrimeX;
                ysvp = ysv * PrimeY;
                zsvp = zsv * PrimeZ;
                wsvp = wsv * PrimeW;
                var ssv = (xsv + ysv + zsv + wsv) * UnSkew4D;
                dx = -xsv - ssv;
                dy = -ysv - ssv;
                dz = -zsv - ssv;
                dw = -wsv - ssv;
            }
        }
